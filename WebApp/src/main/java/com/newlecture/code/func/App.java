package com.newlecture.code.func;

public class App {
	
	// 덧셈 출력
	static void printAdd(){
		int x = 3;
		int y = 4;
		
		int result = x + y;
		System.out.println(result);
	}
	
	// 뺄셈 출력
	static void printSub(){
		int x = 3;
		int y = 4;
		
		int result = x - y;
		System.out.println(result);
	}
	
	public static void main(String[] args) {
		
		// function call
		printAdd();
		
//		---------------------------
		printSub();
//		---------------------------

		printAdd();
		
		
	}

}
